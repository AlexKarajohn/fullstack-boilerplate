/**
 * Writes Supabase local stack URLs and keys to .env.local at repo root.
 * Run after `supabase start` so npm run dev can use them.
 */
const { spawnSync } = require("child_process");
const fs = require("fs");
const path = require("path");

const repoRoot = path.join(__dirname, "..");
const supabaseDir = path.join(repoRoot, "packages", "supabase");

const result = spawnSync("npx", ["supabase", "status", "-o", "json"], {
  cwd: supabaseDir,
  encoding: "utf8",
  shell: true,
});

if (result.status !== 0) {
  console.error("supabase status failed:", result.stderr || result.stdout);
  process.exit(1);
}

// Supabase may print "Using workdir" etc. to stdout; find the JSON block
const raw = result.stdout.trim();
const start = raw.indexOf("{");
const end = raw.lastIndexOf("}") + 1;
if (start === -1 || end === 0) {
  console.error("Could not find JSON in supabase status output");
  process.exit(1);
}

let data;
try {
  data = JSON.parse(raw.slice(start, end));
} catch (e) {
  console.error("Failed to parse supabase status JSON:", e.message);
  process.exit(1);
}

const apiUrl = data.API_URL || data.api_url;
const serviceRoleKey = data.SERVICE_ROLE_KEY || data.service_role_key;
if (!apiUrl || !serviceRoleKey) {
  console.error("Missing API_URL or SERVICE_ROLE_KEY in status output");
  process.exit(1);
}

// SUPABASE_AUTH_URL format: protocol + :SERVICE_ROLE_KEY@host (http for local, https for cloud)
const url = new URL(apiUrl);
const authUrl = `${url.protocol}//:${serviceRoleKey}@${url.host}`;

const dbUrl = data.DB_URL || "";

// Preserve RESEND_FROM, RESEND_* (and other optional) values from existing .env.local so sync does not overwrite them
const envLocalPath = path.join(repoRoot, ".env.local");
let preserved = {};
try {
  const existing = fs.readFileSync(envLocalPath, "utf8");
  for (const line of existing.split("\n")) {
    const m = line.match(/^([A-Z_]+)=(.*)$/);
    if (m) {
      const key = m[1];
      if (key === "RESEND_FROM" || key === "RESEND_API_KEY" || key === "RESEND_HEALTH_TO") {
        preserved[key] = m[2].trim();
      }
    }
  }
} catch {
  // no existing file
}

const lines = [
  "# Auto-generated by scripts/sync-env-local.js (npm run supabase:sync-env)",
  "# Do not commit. Used when running npm run dev with local Supabase.",
  "",
  `SUPABASE_URL=${apiUrl}`,
  `SUPABASE_AUTH_URL=${authUrl}`,
  `DATABASE_URL=${dbUrl}`,
  `RESEND_FROM=${preserved.RESEND_FROM ?? ""}`,
  `RESEND_API_KEY=${preserved.RESEND_API_KEY ?? ""}`,
  `RESEND_HEALTH_TO=${preserved.RESEND_HEALTH_TO ?? ""}`,
  "PORT=3001",
  "",
  "# Optional (for reference):",
  `# STUDIO_URL=${data.STUDIO_URL || "http://127.0.0.1:54323"}`,
  `# ANON_KEY=${data.ANON_KEY || data.PUBLISHABLE_KEY || ""}`,
  "",
];

fs.writeFileSync(envLocalPath, lines.join("\n"), "utf8");
console.log("Wrote", envLocalPath);
